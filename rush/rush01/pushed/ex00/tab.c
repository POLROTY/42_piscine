#include <unistd.h>

void	ft_print_tab(int tab[6][6])
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (i <= 6)
	{
		while (j <= 6)
		{
			write(1, &tab[i][j], 1);
			j++;
		}
		i++;
	}
}

void	ft_tabz(int tab[6][6])
{
	int	i;
	int	j;

	i = 0;
	while (i < 7)
	{
		j = 0;
		while (j < 7)
		{
			tab[i][j] = 0;
			j++;
		}
		i++;
	}
}

void	ft_tab_init(char **av)
{
	int	tab[6][6];

	ft_tabz(tab);
	tab[0][1] = av[1][0] - 48;
	tab[0][2] = av[1][2] - 48;
	tab[0][3] = av[1][4] - 48;
	tab[0][4] = av[1][6] - 48;
	tab[5][1] = av[1][8] - 48;
	tab[5][2] = av[1][10] - 48;
	tab[5][3] = av[1][12] - 48;
	tab[5][4] = av[1][14] - 48;
	tab[1][0] = av[1][16] - 48;
	tab[2][0] = av[1][18] - 48;
	tab[3][0] = av[1][20] - 48;
	tab[4][0] = av[1][22] - 48;
	tab[1][5] = av[1][24] - 48;
	tab[2][5] = av[1][26] - 48;
	tab[3][5] = av[1][28] - 48;
	tab[4][5] = av[1][30] - 48;
	ft_print_tab(tab);
}
