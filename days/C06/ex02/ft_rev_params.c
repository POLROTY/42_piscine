#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	j;

	j = ac;
	while (--j > 0)
	{
		i = -1;
		while (av[j][++i])
			write(1, &av[j][i], 1);
		write(1, "\n", 1);
	}
	return (0);
}
