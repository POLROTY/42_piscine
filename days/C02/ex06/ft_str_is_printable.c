#include <unistd.h>

int	ft_str_is_printable(char *str)
{
	int	i;
	int	j;

	i = 0 ;
	j = 1;
	while (str[i])
	{
		if (str[i] >= 32 && str[i] <= 126)
		{
			i++;
		}
		else
		{
			j = 0;
			i++;
		}
	}
	return (j);
}
