#include <unistd.h>

void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	temp[50];

	i = 0 ;
	while (i < size)
	{
		temp[(size - 1 ) - i] = tab[i];
		i++ ;
	}
	i = 0;
	while (i < size)
	{
		tab[i] = temp[i];
		i++ ;
	}
}
